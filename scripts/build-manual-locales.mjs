function writeLocale(code, data) {
  const missing = allKeys.filter((k) => !data[k]);
  if (missing.length) console.warn(`${code} missing:`, missing);
  const lines = allKeys.map((k) => {
    const v = (data[k] ?? en[k] ?? fr[k] ?? ru[k] ?? k).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    return `  ${k}: '${v}',`;
  });
  const out = `/** ${code.toUpperCase()} — ${allKeys.length} keys */\nexport const ${code} = {\n${lines.join('\n')}\n} as const;\n`;
  fs.mkdirSync('src/i18n/locales', { recursive: true });
  fs.writeFileSync(path.join('src/i18n/locales', `${code}.ts`), out);
  fs.writeFileSync(path.join('scripts/locale-data', `${code}.json`), JSON.stringify(Object.fromEntries(allKeys.map((k) => [k, data[k] ?? en[k]])), null, 2));
  console.log(`${code}: wrote ${allKeys.length} keys`);
}

